package com.imooc.uitl;
/**
 * 一个算法的类
 * @author Administrator
 *
 */
public class Calculate {

	//加法
	public int add(int a, int b) {
		return a + b;
	}

	//减法
	public int subtact(int a, int b) {
		return a - b;
	}

	//乘法
	public int multiply(int a, int b) {
		return a * b;
	}

	//除法
	public int divide(int a, int b) {
		return a / b;
	}
}
